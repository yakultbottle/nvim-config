local function open_project(path)
  vim.cmd('cd ' .. path)
  vim.cmd 'Neotree filesystem reveal current'
end

local function open_neotree()
  vim.cmd 'bd'
  vim.cmd 'Neotree filesystem toggle position=current'
end

